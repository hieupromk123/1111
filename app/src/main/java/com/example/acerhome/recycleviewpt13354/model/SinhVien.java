package com.example.acerhome.recycleviewpt13354.model;

public class SinhVien {
    private String Anh;
    private String Ten;

    public SinhVien(String anh, String ten) {
        Anh = anh;
        Ten = ten;
    }

    public String getAnh() {
        return Anh;
    }

    public void setAnh(String anh) {
        Anh = anh;
    }

    public String getTen() {
        return Ten;
    }

    public void setTen(String ten) {
        Ten = ten;
    }
}
